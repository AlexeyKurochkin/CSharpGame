        public static List<Obstacle> GenerateObstacles(int[,] map)
        {
            List<Obstacle> mapObstacles = new List<Obstacle>();
            for (int i = 0; i < map.GetLength(0); i++)
            {
                for (int j = 0; j < map.GetLength(1); j++)
                {
                    int element = map[i, j];
                    mapObstacles.Add(new Obstacle(element));
                }
            }
            return mapObstacles;
        }
